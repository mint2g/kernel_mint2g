{
	uint8_t temp_reg;
	uint16_t sensor_gain;

	temp_reg = Sensor_ReadReg(0x3000);
	sensor_gain = (16 + (temp_reg & 0x0F));
	if (temp_reg & 0x10)
		sensor_gain <<= 1;
	if (temp_reg & 0x20)
		sensor_gain <<= 1;

	if (temp_reg & 0x40)
		sensor_gain <<= 1;

	if (temp_reg & 0x80)
		sensor_gain <<= 1;

	return sensor_gain;
}				/* read_OV2650_gain */

